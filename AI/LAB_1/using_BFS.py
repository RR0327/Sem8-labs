# Example usage with dynamic input
start_state = (TOTAL_MISSIONARIES, TOTAL_CANNIBALS, "L", 0, 0)
goal_state = (0, 0, "R", TOTAL_MISSIONARIES, TOTAL_CANNIBALS)

# Execution
try:
    solution = bfs(start_state, goal_state)

    if solution:
        print(f"Solution found in {len(solution) - 1} steps:")
        for step_no, step in enumerate(solution):
            print(f"Step {step_no}: {step}")
    else:
        print("No solution found.")
except ValueError as exc:
    print(f"Error: {exc}")